use tauri::command;

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ModalModeInfo {
    pub mode_number: usize,
    pub frequency_hz: f64,
    pub eigenvalue: f64,
}

/// Generate modal analysis input file
#[command]
pub fn generate_modal_analysis_inp(
    nodes: Vec<Node>,
    elements: Vec<Element>,
    material: Material,
    num_modes: usize,
    output_path: String,
) -> Result<String, String> {
    let config = ModalAnalysisConfig {
        num_modes,
        ..Default::default()
    };
    
    generate_modal_inp(&nodes, &elements, &material, &config, &output_path)
        .map_err(|e| e.to_string())
}

/// Run modal analysis solver
#[command]
pub fn run_modal_solver(
    input_file: String,
    working_dir: String,
    num_threads: Option<usize>,
) -> Result<super::solver::SolverResult, String> {
    use super::solver::{CalculiXSolver, SolverConfig};
    
    let config = SolverConfig {
        executable_path: "ccx".to_string(),
        num_threads: num_threads.unwrap_or(4),
        memory_limit_mb: None,
    };
    
    let calc_solver = CalculiXSolver::new(config);
    let input_path = PathBuf::from(&input_file);
    let work_dir = PathBuf::from(&working_dir);
    
    calc_solver.solve(&input_path, &work_dir)
        .map_err(|e| e.to_string())
}

/// Parse modal results from FRD file
#[command]
pub fn parse_modal_results(frd_file: String) -> Result<ModalResults, String> {
    parse_modal_frd(&frd_file)
        .map_err(|e| e.to_string())
}

/// Get list of available modes from results
#[command]
pub fn get_modal_mode_list(frd_file: String) -> Result<Vec<ModalModeInfo>, String> {
    let results = parse_modal_frd(&frd_file)
        .map_err(|e| e.to_string())?;
    
    let modes: Vec<ModalModeInfo> = results.modes.iter()
        .map(|m| ModalModeInfo {
            mode_number: m.mode_number,
            frequency_hz: m.frequency_hz,
            eigenvalue: m.eigenvalue,
        })
        .collect();
    
    Ok(modes)
}

/// Get specific mode shape from results
#[command]
pub fn get_mode_shape(frd_file: String, mode_number: usize) -> Result<ModalMode, String> {
    let results = parse_modal_frd(&frd_file)
        .map_err(|e| e.to_string())?;
    
    results.modes.iter()
        .find(|m| m.mode_number == mode_number)
        .cloned()
        .ok_or_else(|| format!("Mode {} not found", mode_number))
}

/// Export mode shape to displacement result format
#[command]
pub fn export_mode_shape_as_displacement(
    nodes: Vec<Node>,
    frd_file: String,
    mode_number: usize,
    scale: f64,
) -> Result<Vec<NodeDisplacement>, String> {
    let results = parse_modal_frd(&frd_file)
        .map_err(|e| e.to_string())?;
    
    let mode = results.modes.iter()
        .find(|m| m.mode_number == mode_number)
        .ok_or_else(|| format!("Mode {} not found", mode_number))?;
    
    let displacements: Vec<NodeDisplacement> = nodes.iter()
        .map(|n| {
            let shape = mode.node_displacements.iter()
                .find(|s| s.node_id == n.id);
            
            match shape {
                Some(s) => NodeDisplacement {
                    node_id: n.id,
                    x: n.x,
                    y: n.y,
                    z: n.z,
                    ux: s.dx * scale,
                    uy: s.dy * scale,
                    uz: s.dz * scale,
                    magnitude: s.displacement_magnitude * scale,
                },
                None => NodeDisplacement {
                    node_id: n.id,
                    x: n.x,
                    y: n.y,
                    z: n.z,
                    ux: 0.0,
                    uy: 0.0,
                    uz: 0.0,
                    magnitude: 0.0,
                },
            }
        })
        .collect();
    
    Ok(displacements)
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct NodeDisplacement {
    pub node_id: usize,
    pub x: f64,
    pub y: f64,
    pub z: f64,
    pub ux: f64,
    pub uy: f64,
    pub uz: f64,
    pub magnitude: f64,
}

/// Get mode animation keyframes for smooth animation
#[command]
pub fn get_mode_animation_frames(
    nodes: Vec<Node>,
    frd_file: String,
    mode_number: usize,
    num_frames: usize,
    amplitude: f64,
) -> Result<Vec<AnimationFrame>, String> {
    let results = parse_modal_frd(&frd_file)
        .map_err(|e| e.to_string())?;
    
    let mode = results.modes.iter()
        .find(|m| m.mode_number == mode_number)
        .ok_or_else(|| format!("Mode {} not found", mode_number))?;
    
    let mut frames = vec![];
    
    for frame_idx in 0..num_frames {
        let phase = (frame_idx as f64 / num_frames as f64) * 2.0 * std::f64::consts::PI;
        let sin_val = phase.sin();
        
        let displacements: Vec<NodeDisplacement> = nodes.iter()
            .map(|n| {
                let shape = mode.node_displacements.iter()
                    .find(|s| s.node_id == n.id);
                
                match shape {
                    Some(s) => NodeDisplacement {
                        node_id: n.id,
                        x: n.x,
                        y: n.y,
                        z: n.z,
                        ux: s.dx * amplitude * sin_val,
                        uy: s.dy * amplitude * sin_val,
                        uz: s.dz * amplitude * sin_val,
                        magnitude: s.displacement_magnitude * amplitude * sin_val.abs(),
                    },
                    None => NodeDisplacement {
                        node_id: n.id,
                        x: n.x,
                        y: n.y,
                        z: n.z,
                        ux: 0.0,
                        uy: 0.0,
                        uz: 0.0,
                        magnitude: 0.0,
                    },
                }
            })
            .collect();
        
        frames.push(AnimationFrame {
            frame_index: frame_idx,
            phase,
            displacements,
        });
    }
    
    Ok(frames)
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AnimationFrame {
    pub frame_index: usize,
    pub phase: f64,
    pub displacements: Vec<NodeDisplacement>,
}